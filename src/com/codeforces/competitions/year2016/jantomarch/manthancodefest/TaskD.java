	static class Solver
	{
		int n, arr[];
		InputReader in;
		OutputWriter out;

		void solve(int testNumber)
		{
			n = in.nextInt();
			arr = new int[n];

			int zeroes = 0;
			Map<Integer, Integer> map = new HashMap<>();
			Map<Point, Integer> store = new HashMap<>();

			for (int i = 0; i < n; i++)
			{
				arr[i] = in.nextInt();

				Integer temp = map.get(arr[i]);

				if (temp == null)
					map.put(arr[i], 1);
				else
					map.put(arr[i], temp + 1);

				if (arr[i] == 0)
					zeroes++;
			}

			int max = Math.max(2, zeroes);

			Queue<Integer> used = new LinkedList<>();

			for (int i = 0; i < n; i++)
			{
				for (int j = 0; j < n; j++)
				{
					if (i == j)
						continue;

					if (arr[i] == 0 && arr[j] == 0)
						continue;

					Integer val = store.get(new Point(arr[i], arr[j]));

					if (val != null)
						continue;

					int currLen = 2;
					Integer temp;

					temp = map.get(arr[i]);

					if (temp > 1)
						map.put(arr[i], temp - 1);
					else
						map.remove(arr[i]);

					temp = map.get(arr[j]);

					if (temp > 1)
						map.put(arr[j], temp - 1);
					else
						map.remove(arr[j]);

					int first, second, sum;

					first = arr[i];
					second = arr[j];
					used.add(first);
					used.add(second);

					while (true)
					{
						sum = first + second;
						temp = map.get(sum);

						if (temp == null)
							break;

						if (temp == 1)
							map.remove(sum);
						else
							map.put(sum, temp - 1);

						used.add(sum);
						first = second;
						second = sum;
						currLen++;
					}

					store.put(new Point(arr[i], arr[j]), currLen);
					max = Math.max(max, currLen);

					while (used.size() > 0)
					{
						int curr = used.poll();

						temp = map.get(curr);

						if (temp == null)
							map.put(curr, 1);
						else
							map.put(curr, temp + 1);
					}
				}
			}

			out.println(max);
		}

		public Solver(InputReader in, OutputWriter out)
		{
			this.in = in;
			this.out = out;
		}

	}

	static class InputReader
	{
		private InputStream stream;
		private byte[] buf = new byte[1024];
		private int curChar;
		private int numChars;

		public InputReader(InputStream stream)
		{
			this.stream = stream;
		}

		public int read()
		{
			if (numChars == -1)
				throw new InputMismatchException();

			if (curChar >= numChars)
			{
				curChar = 0;
				try
				{
					numChars = stream.read(buf);
				}
				catch (IOException e)
				{
					throw new InputMismatchException();
				}
				if (numChars <= 0)
					return -1;
			}

			return buf[curChar++];
		}

		public int nextInt()
		{
			int c = read();

			while (isSpaceChar(c))
				c = read();

			int sgn = 1;

			if (c == '-')
			{
				sgn = -1;
				c = read();
			}

			int res = 0;

			do
			{
				if (c < '0' || c > '9')
					throw new InputMismatchException();

				res *= 10;
				res += c & 15;

				c = read();
			} while (!isSpaceChar(c));

			return res * sgn;
		}

		public int[] nextIntArray(int arraySize)
		{
			int array[] = new int[arraySize];

			for (int i = 0; i < arraySize; i++)
				array[i] = nextInt();

			return array;
		}

		public long nextLong()
		{
			int c = read();

			while (isSpaceChar(c))
				c = read();

			int sign = 1;

			if (c == '-')
			{
				sign = -1;

				c = read();
			}

			long result = 0;

			do
			{
				if (c < '0' || c > '9')
					throw new InputMismatchException();

				result *= 10;
				result += c & 15;

				c = read();
			} while (!isSpaceChar(c));

			return result * sign;
		}

		public long[] nextLongArray(int arraySize)
		{
			long array[] = new long[arraySize];

			for (int i = 0; i < arraySize; i++)
				array[i] = nextLong();

			return array;
		}

		public float nextFloat() // problematic
		{
			float result, div;
			byte c;

			result = 0;
			div = 1;
			c = (byte) read();

			while (c <= ' ')
				c = (byte) read();

			boolean isNegative = (c == '-');

			if (isNegative)
				c = (byte) read();

			do
			{
				result = result * 10 + c - '0';
			} while ((c = (byte) read()) >= '0' && c <= '9');

			if (c == '.')
				while ((c = (byte) read()) >= '0' && c <= '9')
					result += (c - '0') / (div *= 10);

			if (isNegative)
				return -result;

			return result;
		}

		public double nextDouble() // not completely accurate
		{
			double ret = 0, div = 1;
			byte c = (byte) read();

			while (c <= ' ')
				c = (byte) read();

			boolean neg = (c == '-');

			if (neg)
				c = (byte) read();

			do
			{
				ret = ret * 10 + c - '0';
			} while ((c = (byte) read()) >= '0' && c <= '9');

			if (c == '.')
				while ((c = (byte) read()) >= '0' && c <= '9')
					ret += (c - '0') / (div *= 10);

			if (neg)
				return -ret;

			return ret;
		}

		public String next()
		{
			int c = read();

			while (isSpaceChar(c))
				c = read();

			StringBuilder res = new StringBuilder();

			do
			{
				res.appendCodePoint(c);

				c = read();
			} while (!isSpaceChar(c));

			return res.toString();
		}

		public boolean isSpaceChar(int c)
		{
			return c == ' ' || c == '\n' || c == '\r' || c == '\t' || c == -1;
		}

		public String nextLine()
		{
			int c = read();

			StringBuilder result = new StringBuilder();

			do
			{
				result.appendCodePoint(c);

				c = read();
			} while (!isNewLine(c));

			return result.toString();
		}

		public boolean isNewLine(int c)
		{
			return c == '\n';
		}

		public void close()
		{
			try
			{
				stream.close();
			}
			catch (IOException e)
			{
				e.printStackTrace();
			}
		}

	}


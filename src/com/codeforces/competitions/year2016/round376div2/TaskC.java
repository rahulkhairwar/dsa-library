    public static void main(String[] args)
    {
        InputReader in = new InputReader(System.in);
        OutputWriter out = new OutputWriter(System.out);
		Solver solver = new Solver(in, out);

		solver.solve();
        in.close();
        out.flush();
        out.close();
    }

    static class Solver
    {
        int n, m, k;
		int[] col;
		Node[] nodes;
        InputReader in;
        OutputWriter out;

		void solve()
		{
			n = in.nextInt();
			m = in.nextInt();
			k = in.nextInt();
			col = new int[n];
			nodes = new Node[n];

			for (int i = 0; i < n; i++)
				col[i] = in.nextInt();

			for (int i = 0; i < n; i++)
				nodes[i] = new Node(i, col[i]);

			for (int i = 0; i < m; i++)
			{
				int l, r;

				l = in.nextInt() - 1;
				r = in.nextInt() - 1;
				union(nodes[l], nodes[r]);
			}

			Set<Integer> set = new HashSet<>();

			for (int i = 0; i < n; i++)
			{
				Node par = findParent(nodes[i]);

				add(par, nodes[i]);
				set.add(par.key);
			}

			Iterator<Integer> iterator = set.iterator();
			int total = 0;

			while (iterator.hasNext())
			{
				int curr = iterator.next();
				Iterator<Map.Entry<Integer, Integer>> it = nodes[curr].map.entrySet().iterator();
				int max = 0;

				while (it.hasNext())
					max = Math.max(max, it.next().getValue());

				total += nodes[curr].size - max;
			}

			out.println(total);
		}

		void add(Node node, Node other)
		{
			node.map.put(other.color, node.map.getOrDefault(other.color, 0) + 1);
		}

		Node findParent(Node node)
		{
			if (node.equals(node.parent))
				return node;
			else
				return node.parent = findParent(node.parent);
		}

		void union(Node one, Node two)
		{
			Node oneParent, twoParent;

			oneParent = findParent(one);
			twoParent = findParent(two);

			if (oneParent.equals(twoParent))
				return;

			if (oneParent.height > twoParent.height)
			{
				twoParent.parent = oneParent;
				oneParent.size += twoParent.size;
			}
			else if (oneParent.height < twoParent.height)
			{
				oneParent.parent = twoParent;
				twoParent.size += oneParent.size;
			}
			else
			{
				twoParent.parent = oneParent;
				oneParent.size += twoParent.size;
				oneParent.height++;
			}
		}

		class Node
		{
			int key, color, size, height;
			Map<Integer, Integer> map;
			Node parent;

			public Node(int key, int color)
			{
				this.key = key;
				this.color = color;
				size = 1;
				map = new HashMap<>();
				parent = this;
			}

			@Override public boolean equals(Object obj)
			{
				Node node = (Node) obj;

				return key == node.key;
			}

		}

        public Solver(InputReader in, OutputWriter out)
        {
        	this.in = in;
        	this.out = out;
        }

	}

    static class InputReader
    {
        private InputStream stream;
        private byte[] buf = new byte[1024];
        private int curChar;
        private int numChars;

        public InputReader(InputStream stream)
        {
            this.stream = stream;
        }

        public int read()
        {
            if (numChars == -1)
                throw new InputMismatchException();

            if (curChar >= numChars)
            {
                curChar = 0;
                try
                {
                    numChars = stream.read(buf);
                } catch (IOException e)
                {
                    throw new InputMismatchException();
                }
                if (numChars <= 0)
                    return -1;
            }

            return buf[curChar++];
        }

        public int nextInt()
        {
            int c = read();

            while (isSpaceChar(c))
                c = read();

            int sgn = 1;

            if (c == '-')
            {
                sgn = -1;
                c = read();
            }

            int res = 0;

            do
            {
                if (c < '0' || c > '9')
                    throw new InputMismatchException();

                res *= 10;
                res += c & 15;

                c = read();
            } while (!isSpaceChar(c));

            return res * sgn;
        }

        public int[] nextIntArray(int arraySize)
        {
            int array[] = new int[arraySize];

            for (int i = 0; i < arraySize; i++)
                array[i] = nextInt();

            return array;
        }

        public long nextLong()
        {
            int c = read();

            while (isSpaceChar(c))
                c = read();

            int sign = 1;

            if (c == '-')
            {
                sign = -1;

                c = read();
            }

            long result = 0;

            do
            {
                if (c < '0' || c > '9')
                    throw new InputMismatchException();

                result *= 10;
                result += c & 15;

                c = read();
            } while (!isSpaceChar(c));

            return result * sign;
        }

        public long[] nextLongArray(int arraySize)
        {
            long array[] = new long[arraySize];

            for (int i = 0; i < arraySize; i++)
                array[i] = nextLong();

            return array;
        }

        public float nextFloat()
        {
            float result, div;
            byte c;

            result = 0;
            div = 1;
            c = (byte) read();

            while (c <= ' ')
                c = (byte) read();

            boolean isNegative = (c == '-');

            if (isNegative)
                c = (byte) read();

            do
            {
                result = result * 10 + c - '0';
            } while ((c = (byte) read()) >= '0' && c <= '9');

            if (c == '.')
                while ((c = (byte) read()) >= '0' && c <= '9')
                    result += (c - '0') / (div *= 10);

            if (isNegative)
                return -result;

            return result;
        }

        public double nextDouble()
        {
            double ret = 0, div = 1;
            byte c = (byte) read();

            while (c <= ' ')
                c = (byte) read();

            boolean neg = (c == '-');

            if (neg)
                c = (byte) read();

            do
            {
                ret = ret * 10 + c - '0';
            } while ((c = (byte) read()) >= '0' && c <= '9');

            if (c == '.')
                while ((c = (byte) read()) >= '0' && c <= '9')
                    ret += (c - '0') / (div *= 10);

            if (neg)
                return -ret;

            return ret;
        }

        public String next()
        {
            int c = read();

            while (isSpaceChar(c))
                c = read();

            StringBuilder res = new StringBuilder();

            do
            {
                res.appendCodePoint(c);

                c = read();
            } while (!isSpaceChar(c));

            return res.toString();
        }

        public boolean isSpaceChar(int c)
        {
            return c == ' ' || c == '\n' || c == '\r' || c == '\t' || c == -1;
        }

        public String nextLine()
        {
            int c = read();

            StringBuilder result = new StringBuilder();

            do
            {
                result.appendCodePoint(c);

                c = read();
            } while (!isNewLine(c));

            return result.toString();
        }

        public boolean isNewLine(int c)
        {
            return c == '\n';
        }

        public void close()
        {
            try
            {
                stream.close();
            } catch (IOException e)
            {
                e.printStackTrace();
            }
        }

    }

